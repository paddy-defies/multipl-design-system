/// Multipl design system spacing tokens (8px grid).
abstract final class AppSpacing {
  // ── Base scale ─────────────────────────────────────────────────────────────
  static const double s2 = 2.0;
  static const double s4 = 4.0;
  static const double s6 = 6.0;
  static const double s8 = 8.0;
  static const double s10 = 10.0;
  static const double s12 = 12.0;
  static const double s14 = 14.0;
  static const double s16 = 16.0;
  static const double s20 = 20.0;
  static const double s24 = 24.0;
  static const double s28 = 28.0;
  static const double s32 = 32.0;
  static const double s40 = 40.0;
  static const double s48 = 48.0;
  static const double s56 = 56.0;
  static const double s64 = 64.0;

  // ── Semantic aliases ───────────────────────────────────────────────────────
  /// Horizontal page margin (16px)
  static const double pagePadding = s16;

  /// Gap between major sections (32px)
  static const double sectionGap = s32;

  /// Gap between list items / cards (12px)
  static const double itemGap = s12;

  /// Internal card padding (16px)
  static const double cardPadding = s16;

  /// Compact spacing within a row (8px)
  static const double rowGap = s8;

  /// Tight inline gap (4px)
  static const double inlineGap = s4;

  // ── Button heights ─────────────────────────────────────────────────────────
  static const double buttonHeight = 52.0;
  static const double buttonHeightSmall = 44.0;
  static const double chipHeight = 36.0;
  static const double inputHeight = 56.0;
}
